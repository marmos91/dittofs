func TestOptsValidateDefaults(t *testing.T) {
	o := Opts{}
	if err := o.Validate(); err != nil {
		t.Fatal(err)
	}
	if len(o.Conc) != 4 || o.Conc[3] != 64 {
		t.Fatalf("default conc sweep = %v", o.Conc)
	}
	if len(o.Quadrants) != len(AllQuadrants) {
		t.Fatalf("default quadrants = %v", o.Quadrants)
	}
	if o.LargeFileBytes != 1<<30 || o.SmallFileBytes != 64<<10 {
		t.Fatalf("default sizes = %d/%d", o.LargeFileBytes, o.SmallFileBytes)
	}
}

